# Write a program that uses a hash to store a list of movie titles with the year they came out. 
# Then use the puts command to make your program print out the year of each movie to the screen.
##

movies = {:Jaws => '1975', 
          :Saw => '2004', 
          :Frozen => '2013', 
          :Shrek => '2001', 
          :Stripes => '1981'}

puts movies[:Jaws]
puts movies[:Saw]
puts movies[:Frozen]
puts movies[:Shrek]
puts movies[:Stripes]